def load_country(name: str) -> FactbookDict:
    """Load a country's factbook data by its friendly name.

    Returns a FactbookDict wrapping the JSON data.
    Results are cached after first load.
    """
    if name in _country_cache:
        return _country_cache[name]

    if name not in COUNTRY_REGISTRY:
        available = sorted(set(COUNTRY_REGISTRY.keys()))
        raise ValueError(
            f"Unknown country '{name}'. Available: {', '.join(available)}"
        )

    region, stem = COUNTRY_REGISTRY[name]

    from ._wrapper import FactbookDict

    data_dir = resources.files("cia_world_factbook") / "data" / region
    json_file = data_dir / f"{stem}.json"

    raw = json.loads(json_file.read_text(encoding="utf-8"))

    wrapped = FactbookDict(raw)
    _country_cache[name] = wrapped
    return wrapped
